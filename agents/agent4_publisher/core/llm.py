"""Минимальный LLM-модуль Агента 4.

Переносим не весь `sales_ai`, а только полезную идею: брать тему, студийный
промпт и делать черновик поста. Курс, Jarvis и прогрев сюда не попадают.
"""

from __future__ import annotations

from datetime import datetime
from pathlib import Path
import re

from agents.agent4_publisher.core.config import (
    AUDIENCE_FEARS_FILE,
    CONTENT_PATTERNS_FILE,
    CUSTOMER_PROFILES_FILE,
    OUT_POSTS,
    STYLE_NOTES_FILE,
    SYSTEM_PROMPT_FILE,
)
from shared.llm_client import LLMConfigError, complete_text


def _read_optional(path: Path) -> str:
    return path.read_text(encoding="utf-8").strip() if path.exists() else ""


def _slug(text: str) -> str:
    cleaned = re.sub(r"[^a-zA-Z0-9а-яА-ЯёЁ]+", "-", text).strip("-").lower()
    return cleaned[:60] or "post"


def build_prompt(topic: str) -> str:
    system_prompt = _read_optional(SYSTEM_PROMPT_FILE)
    fears = _read_optional(AUDIENCE_FEARS_FILE)
    profiles = _read_optional(CUSTOMER_PROFILES_FILE)
    style_notes = _read_optional(STYLE_NOTES_FILE)
    content_patterns = _read_optional(CONTENT_PATTERNS_FILE)
    return f"""
{system_prompt}

## Правила стиля `СИЛА Проекта`
{style_notes}

## Библиотека разных форматов
{content_patterns}

## База страхов аудитории
{fears}

## Профили заказчиков
{profiles}

## Задача
Напиши один черновик поста для MAX/Telegram-канала `СИЛА Проекта` на тему: {topic}

Важно про разнообразие:
- не пиши все посты по одному сценарию;
- не начинай каждый пост с `Представьте`;
- не используй каждый раз схему `ситуация -> риск -> чеклист -> CTA`;
- не меняй только начало, оставляя тот же финал;
- не заканчивай каждый формат одинаковой связкой `почему это важно -> КП -> пришлите план`;
- основной рабочий формат для CTR: `разбор живой фразы клиента`, но его нельзя использовать подряд бесконечно;
- молча выбери один формат из библиотеки форматов и адаптируй его под тему, стадию воронки и путь монетизации;
- если тема про склад или КП, не обязательно делать историю. Можно сделать диалог, ошибку, чеклист, миф, сравнение или разбор термина;
- текст должен вести не просто к чтению, а к одному измеримому действию: вопрос, сохранение, репост, присланный план, описание объекта, заявка, КП, сделка или сигнал спроса;
- в ответе не называй выбранный формат служебно. Просто напиши пост.

Общие обязательные элементы, но не обязательный порядок:
- конкретный герой или адресат: собственник, директор, арендатор, застройщик, ГИП, подрядчик или частный клиент;
- конкретный сегмент: склад, ангар, реконструкция, коммерческое помещение, ПД/РД, КР/КЖ/КМ или перепланировка;
- один полезный смысл;
- один честный ограничитель, если он естественен для выбранного формата;
- один финальный сигнал, который соответствует формату: вопрос, сохранение, репост, наблюдение, присланный план, описание объекта, заявка, КП, сделка или сигнал спроса;
- один путь монетизации: лид, квалификация, КП, сделка, доверие к каналу, сохранение, репост, партнёрский контакт или сигнал спроса.

B2B-сегменты ВПП, которые нужно учитывать:
- коммерческая перепланировка, retail, офисы, салоны, помещения в ТЦ;
- склады, ангары, производственные и логистические помещения;
- реконструкция школ, детских садов, МКД, общественных и коммерческих зданий;
- проектная документация, рабочая документация, сопровождение экспертизы;
- отдельные разделы КР, КЖ, КМ;
- частные дома и квартирные перепланировки как вторичный поток.

Стоп-правила:
- не обещай гарантированное согласование, прохождение экспертизы, точную цену или срок без исходных данных;
- не используй рекламные общие фразы: `мы всегда готовы помочь`, `не стоит рисковать`, `лучшее решение`, `быстро и дешево`, `под ключ без проблем`, `без лишних проблем`, `избежать неприятных сюрпризов`, `получить консультацию`;
- не начинай с `мы оказываем услуги`;
- не делай обязательную форму, квиз, email или телефон;
- не пиши `СНиПы` как универсальное слово; используй аккуратно: нормы, СП, ГОСТ, требования экспертизы или согласования;
- не используй длинное тире `—`; если нужна пауза, разбей мысль на два коротких предложения или поставь двоеточие;
- не добавляй персональные данные, адреса, кадастровые номера, суммы договоров и неподтвержденные кейсы;
- не упоминай, что текст создан агентом, промптом или системой.

CTA или финал:
- не пиши общий CTA `напишите нам за консультацией`;
- не каждый пост обязан заканчиваться просьбой прислать план;
- финал должен соответствовать выбранному формату;
- для дневника проектировщика можно закончить наблюдением, заметкой дня, сохранением поста или вопросом к читателю;
- для красного флага финал должен быть коротким;
- для анти-чеклиста финал должен быть самопроверкой;
- для основного формата можно вести к исходным данным, но не использовать этот финал во всех форматах.

Формат результата:
- только текст поста, без служебных комментариев;
- 900-1800 символов;
- короткие абзацы;
- один главный CTA в конце.
""".strip()


def generate_post_text(topic: str, dry_run: bool = False) -> str:
    prompt = build_prompt(topic)
    if dry_run:
        return (
            f"# Черновик поста: {topic}\n\n"
            "Это dry-run без обращения к API.\n\n"
            "Проверяется prompt/style слой Agent 4 с разными форматами постов.\n\n"
            "Agent 4 больше не должен писать все посты по одной схеме. "
            "Основной формат: разбор живой фразы клиента. Дополнительные форматы: вопрос-ответ, "
            "анти-чеклист, дневник проектировщика, мини-кейс, термин, два пути, красный флаг, "
            "карусель и позиция эксперта.\n\n"
            "Любой формат должен идти по воронке к лиду, диалогу, заявке, КП, сделке, доверию, "
            "сохранению, репосту или другому измеримому пути монетизации.\n\n"
            "Стоп-правила: без гарантированных обещаний, без точной цены без исходных данных, "
            "без общих рекламных фраз и без реальной публикации.\n"
        )

    try:
        return complete_text(
            task="content",
            max_tokens=900,
            temperature=0.7,
            system="Ты пишешь контент для студии проектирования ВПП.",
            user=prompt,
        )
    except LLMConfigError as exc:
        raise RuntimeError(
            "LLM-router не настроен для Agent 4. Проверь LLM_PROVIDER и ключ провайдера в `.env`."
        ) from exc


def save_post(topic: str, text: str, output: str | None = None) -> Path:
    if output:
        path = Path(output)
    else:
        stamp = datetime.now().strftime("%Y%m%d-%H%M%S")
        path = OUT_POSTS / f"{stamp}-{_slug(topic)}.md"
    path.parent.mkdir(parents=True, exist_ok=True)
    path.write_text(text.strip() + "\n", encoding="utf-8")
    return path
